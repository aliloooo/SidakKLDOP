export const getTargetByRO = (roName) => {
    // roName might be "RO 5/ Bandar Lampung" or just "Bandar Lampung"
    const normalizedRO = roName.includes('/') ? roName.split('/')[1].trim() : roName.trim();
    // Special case for Jakarta numbering if needed, but let's try direct match first
    const key = Object.keys(TARGET_DATA).find(k => k.toLowerCase() === normalizedRO.toLowerCase());
    return TARGET_DATA[key] || null;
};
